import { createHash } from "node:crypto";
import { execFile } from "node:child_process";
import { mkdtemp, rm, writeFile } from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);

export const NATIVE_DECLARATIVE_EVOLUTION_REGISTRY = "openclaw-native-declarative-evolution-candidate-v0";
export const NATIVE_DECLARATIVE_EVOLUTION_TARGET = "/etc/nixos/openclaw-managed.nix";
const MAX_CHANGES = 8;
const MAX_VALIDATION_OUTPUT = 512;
const DEFAULT_CAPTURE_DURATION_MS = 1000;
const DEFAULT_CAPTURE_MAX_EVENTS = 128;
const ALLOWED_COMPONENTS = new Set([
  "eventHub",
  "core",
  "sessionManager",
  "browserRuntime",
  "screenSense",
  "screenAct",
  "systemSense",
  "systemHeal",
  "observerUi",
]);
const ALLOWED_OPERATIONS = new Set([
  "enable_component",
  "enable_kernel_event_capture",
  "set_kernel_event_capture_limits",
]);

function boundedInteger(value, fallback, minimum, maximum, field) {
  const candidate = value === undefined || value === null || value === ""
    ? fallback
    : Number(value);
  if (!Number.isInteger(candidate) || candidate < minimum || candidate > maximum) {
    throw new Error(`${field} must be an integer between ${minimum} and ${maximum}.`);
  }
  return candidate;
}

function normaliseChanges(changes) {
  if (!Array.isArray(changes) || changes.length === 0) {
    throw new Error("At least one declarative evolution change is required.");
  }
  if (changes.length > MAX_CHANGES) {
    throw new Error(`Declarative evolution candidates accept at most ${MAX_CHANGES} changes.`);
  }

  const normalised = changes.map((change, index) => {
    if (!change || typeof change !== "object" || Array.isArray(change)) {
      throw new Error(`Declarative evolution change ${index + 1} must be an object.`);
    }
    const operation = typeof change.operation === "string" ? change.operation.trim() : "";
    if (!ALLOWED_OPERATIONS.has(operation)) {
      throw new Error(`Declarative evolution operation ${operation || "<empty>"} is not allowlisted.`);
    }

    const allowedKeys = operation === "enable_component"
      ? new Set(["operation", "component"])
      : new Set(["operation", "durationMs", "maxEvents"]);
    if (Object.keys(change).some((key) => !allowedKeys.has(key))) {
      throw new Error(`Declarative evolution change ${operation} contains an unsupported field.`);
    }

    if (operation === "enable_component") {
      const component = typeof change.component === "string" ? change.component.trim() : "";
      if (!ALLOWED_COMPONENTS.has(component)) {
        throw new Error(`Declarative evolution component ${component || "<empty>"} is not allowlisted.`);
      }
      return { operation, component };
    }

    return {
      operation,
      durationMs: boundedInteger(
        change.durationMs,
        DEFAULT_CAPTURE_DURATION_MS,
        1,
        5000,
        "durationMs",
      ),
      maxEvents: boundedInteger(
        change.maxEvents,
        DEFAULT_CAPTURE_MAX_EVENTS,
        1,
        4096,
        "maxEvents",
      ),
    };
  });

  const duplicateKeys = new Set();
  for (const change of normalised) {
    const key = change.operation === "enable_component"
      ? `${change.operation}:${change.component}`
      : change.operation;
    if (duplicateKeys.has(key)) {
      throw new Error(`Declarative evolution change ${key} is duplicated.`);
    }
    duplicateKeys.add(key);
  }

  return normalised.sort((left, right) => JSON.stringify(left).localeCompare(JSON.stringify(right)));
}

function renderNixString(value) {
  return JSON.stringify(value);
}

function renderCandidate(changes) {
  const componentChanges = changes
    .filter((change) => change.operation === "enable_component")
    .map((change) => change.component);
  const kernelChange = changes.find((change) => (
    change.operation === "enable_kernel_event_capture"
    || change.operation === "set_kernel_event_capture_limits"
  ));

  const lines = [
    "# Generated by OpenClaw declarative evolution candidate v0.",
    "# Review and stage explicitly; OpenClaw does not write or activate this file.",
    "{ lib, ... }:",
    "{",
  ];

  if (componentChanges.length > 0) {
    lines.push(
      "  services.openclaw.components = lib.mkAfter [",
      ...componentChanges.map((component) => `    ${renderNixString(component)}`),
      "  ];",
    );
  }

  if (kernelChange) {
    lines.push(
      "  services.openclaw.kernelEventCapture = {",
      ...(kernelChange.operation === "enable_kernel_event_capture" ? ["    enable = true;"] : []),
      `    durationMs = ${kernelChange.durationMs};`,
      `    maxEvents = ${kernelChange.maxEvents};`,
      "  };",
    );
  }

  lines.push("}", "");
  return lines.join("\n");
}

function hashCandidate(candidateText) {
  return createHash("sha256").update(candidateText, "utf8").digest("hex");
}

function boundedOutput(value) {
  return typeof value === "string" ? value.trim().slice(0, MAX_VALIDATION_OUTPUT) : "";
}

export async function validateNativeDeclarativeEvolutionNix(candidateText, {
  nixInstantiate = process.env.OPENCLAW_NIX_INSTANTIATE ?? "nix-instantiate",
  timeoutMs = 5000,
} = {}) {
  const tempDir = await mkdtemp(path.join(os.tmpdir(), "openclaw-managed-nix-"));
  const candidatePath = path.join(tempDir, "openclaw-managed.nix");
  try {
    await writeFile(candidatePath, candidateText, { encoding: "utf8", mode: 0o600 });
    const expression = [
      `let module = import ${renderNixString(candidatePath)};`,
      "value = module { lib = { mkAfter = value: value; }; };",
      "in builtins.deepSeq value (builtins.typeOf value)",
    ].join(" ");
    const { stdout, stderr } = await execFileAsync(nixInstantiate, [
      "--eval",
      "--json",
      "--strict",
      "--expr",
      expression,
    ], {
      timeout: timeoutMs,
      maxBuffer: 16 * 1024,
    });
    const resultType = JSON.parse(stdout.trim());
    return resultType === "set"
      ? { status: "passed", mode: "nix-instantiate", resultType }
      : { status: "failed", mode: "nix-instantiate", reason: "unexpected_result_type", resultType };
  } catch (error) {
    if (error?.code === "ENOENT") {
      return {
        status: "unavailable",
        mode: "nix-instantiate",
        reason: "nix_instantiate_unavailable",
      };
    }
    return {
      status: "failed",
      mode: "nix-instantiate",
      reason: "nix_validation_failed",
      stderr: boundedOutput(error?.stderr),
      message: boundedOutput(error?.message),
    };
  } finally {
    await rm(tempDir, { recursive: true, force: true });
  }
}

export function createNativeDeclarativeEvolutionBuilders({
  validateCandidate = validateNativeDeclarativeEvolutionNix,
} = {}) {
  async function buildNativeDeclarativeEvolutionCandidate({ changes = [] } = {}) {
    const normalisedChanges = normaliseChanges(changes);
    const candidateText = renderCandidate(normalisedChanges);
    const candidateHash = hashCandidate(candidateText);
    const validation = await validateCandidate(candidateText);
    const usable = validation?.status === "passed";

    return {
      ok: true,
      registry: NATIVE_DECLARATIVE_EVOLUTION_REGISTRY,
      mode: "managed-nix-candidate-validation",
      candidateStatus: usable ? "validated" : "blocked",
      target: {
        path: NATIVE_DECLARATIVE_EVOLUTION_TARGET,
        activation: "manual-only",
      },
      changes: normalisedChanges,
      candidateText,
      candidateHash,
      candidateBytes: Buffer.byteLength(candidateText, "utf8"),
      validation: validation ?? { status: "unavailable", reason: "validator_missing" },
      governance: {
        candidateGeneration: true,
        writesManagedConfig: false,
        runsNixValidation: validation?.mode === "nix-instantiate",
        switchesGeneration: false,
        executesRollback: false,
        createsTask: false,
        createsApproval: false,
        readsCredentials: false,
        callsProvider: false,
        networkEgress: false,
      },
      next: {
        recommendedAction: usable ? "operator_review_then_explicit_stage" : "fix_candidate_validation",
        stageOwner: "future_approved_declarative_evolution_task",
        automaticStage: false,
        automaticSwitch: false,
        automaticRollback: false,
      },
    };
  }

  return { buildNativeDeclarativeEvolutionCandidate };
}
